def a(n):
    lst=[]
    for i in range(2, (n//2)+1):
        for j in lst:
            if i % j == 0:
                break
        else:
            lst.append(i)
    return lst

def mul(a, b):
    return a * b

flag = True
lst = []

n = 600
summ = n

d = a(n)
print(d)

while flag:
    if summ == 1:
        flag = False
    else:
        for i in d:
            if summ % i == 0:
                lst.append(i)
                summ /= i
                continue
print(lst)
print(reduce(mul,lst))

