k = int(input("Введите число: "))

while k >= 0:
     num = randint(0, 101)
     if num == 0 and k == 0: print(" = 0")
     elif num == 0:
         k -= 1
         continue
     if k == 0: print(f"{num} = 0")
     elif k == 1: print(f"{num}x + ", end = "")
     else: print(f"{num}x**{k} + ", end = "")
